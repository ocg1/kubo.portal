package mx.com.kubo.model.investor;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Embeddable;

@Embeddable
public class OtherinvestmentCatPK implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	@Column
	private int company_id; //` TINYINT(3) NOT NULL,
	@Column
	private int other_investment_id;
	public int getCompany_id() {
		return company_id;
	}
	public void setCompany_id(int company_id) {
		this.company_id = company_id;
	}
	public int getOther_investment_id() {
		return other_investment_id;
	}
	public void setOther_investment_id(int other_investment_id) {
		this.other_investment_id = other_investment_id;
	}
	
}
