package mx.com.kubo.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Embeddable;

@Embeddable
public class ViewInvestmetInProyect_PK implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	@Column
	private Integer solicitud_id;
	@Column
	private Integer credito_id;
	@Column
	private String investor;
	
	public Integer getSolicitud_id() {
		return solicitud_id;
	}
	public void setSolicitud_id(Integer solicitud_id) {
		this.solicitud_id = solicitud_id;
	}
	public Integer getCredito_id() {
		return credito_id;
	}
	public void setCredito_id(Integer credito_id) {
		this.credito_id = credito_id;
	}
	public String getInvestor() {
		return investor;
	}
	public void setInvestor(String investor) {
		this.investor = investor;
	}
	
}
