package mx.com.kubo.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Embeddable;

@Embeddable
public class LoanTypePK 
implements Serializable 
{
	private static final long serialVersionUID = 7109900116785814788L;
	
	@Column private int company_id;
	
	@Column private String loan_type_id;
	
	public int getCompany_id() 
	{
		return company_id;
	}
	
	public void setCompany_id(int company_id) 
	{
		this.company_id = company_id;
	}
	
	public String getLoan_type_id() 
	{
		return loan_type_id;
	}
	
	public void setLoan_type_id(String loan_type_id) 
	{
		this.loan_type_id = loan_type_id;
	}
}
