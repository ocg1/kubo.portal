package mx.com.kubo.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Embeddable;

@Embeddable
public class ConsultingManualPK implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	@Column
	private int manual_consulting_id;//	int(10)	NO
	@Column
	private int prospectus_id;//	int(10)	NO
	@Column
	private int company_id;//	tinyint(3)	NO
	
	public int getManual_consulting_id() {
		return manual_consulting_id;
	}
	public void setManual_consulting_id(int manual_consulting_id) {
		this.manual_consulting_id = manual_consulting_id;
	}
	public int getProspectus_id() {
		return prospectus_id;
	}
	public void setProspectus_id(int prospectus_id) {
		this.prospectus_id = prospectus_id;
	}
	public int getCompany_id() {
		return company_id;
	}
	public void setCompany_id(int company_id) {
		this.company_id = company_id;
	}
}
