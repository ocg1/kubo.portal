package mx.com.kubo.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Embeddable;

@Embeddable
public class InegiEconActivityCatPK implements Serializable {

	private static final long serialVersionUID = 1L;
	
	@Column
	private int inegi_econ_activity_id;
	@Column
	private int company_id;
	
	
	public InegiEconActivityCatPK(){
		
	}
	public InegiEconActivityCatPK(int inegi_econ_activity_id,int company_id){
		this.inegi_econ_activity_id=inegi_econ_activity_id;
		this.company_id=company_id;
	}
	public int getInegi_econ_activity_id() {
		return inegi_econ_activity_id;
	}
	public void setInegi_econ_activity_id(int inegi_econ_activity_id) {
		this.inegi_econ_activity_id = inegi_econ_activity_id;
	}
	public int getCompany_id() {
		return company_id;
	}
	public void setCompany_id(int company_id) {
		this.company_id = company_id;
	}

}
