package mx.com.kubo.model.mesa.solicitud.notas;

import java.io.Serializable;

@SuppressWarnings("serial")
public class MetaInfoToken 
implements Serializable
{
	private String field_name;
	private String field_value;
	
	public MetaInfoToken(String name, String value)
	{
		field_name  = name;
		field_value = value;
	}
	
	public final String getField_name() 
	{
		return field_name;
	}
	
	public final String getField_value() 
	{
		return field_value;
	}
	
	public final void setField_name(String name) 
	{
		field_name = name;
	}
	
	public final void setField_value(String value) 
	{
		field_value = value;
	}
}
