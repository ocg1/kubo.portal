package mx.com.kubo.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Embeddable;

@Embeddable
public class ReferredPK implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	@Column
	private int prospectus_id_origin; 
	@Column
	private int prospectus_id_destiny;
	@Column
	private int company_id;
	
	
	
	public int getProspectus_id_origin() {
		return prospectus_id_origin;
	}
	public void setProspectus_id_origin(int prospectus_id_origin) {
		this.prospectus_id_origin = prospectus_id_origin;
	}
	public int getProspectus_id_destiny() {
		return prospectus_id_destiny;
	}
	public void setProspectus_id_destiny(int prospectus_id_destiny) {
		this.prospectus_id_destiny = prospectus_id_destiny;
	}
	public int getCompany_id() {
		return company_id;
	}
	public void setCompany_id(int company_id) {
		this.company_id = company_id;
	}
}
