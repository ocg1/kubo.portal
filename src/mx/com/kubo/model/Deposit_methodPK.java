package mx.com.kubo.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Embeddable;

@Embeddable
public class Deposit_methodPK implements Serializable {
	private static final long serialVersionUID = 1L;
	
	@Column
	private int deposit_method_id;
	@Column
	private int company_id;
	
	public Deposit_methodPK(){
		
	}

	public int getDeposit_method_id() {
		return deposit_method_id;
	}

	public void setDeposit_method_id(int deposit_method_id) {
		this.deposit_method_id = deposit_method_id;
	}

	public int getCompany_id() {
		return company_id;
	}

	public void setCompany_id(int company_id) {
		this.company_id = company_id;
	}

	
	
}
