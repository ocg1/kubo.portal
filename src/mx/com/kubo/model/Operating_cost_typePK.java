package mx.com.kubo.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Embeddable;

@Embeddable
public class Operating_cost_typePK implements Serializable {
	
	private static final long serialVersionUID = 1L;
	@Column
	private int operating_cost_type_id;
	@Column
	private int company_id;
	
	public Operating_cost_typePK(){
		
	}

	public int getOperating_cost_type_id() {
		return operating_cost_type_id;
	}

	public void setOperating_cost_type_id(int operating_cost_type_id) {
		this.operating_cost_type_id = operating_cost_type_id;
	}

	public int getCompany_id() {
		return company_id;
	}

	public void setCompany_id(int company_id) {
		this.company_id = company_id;
	}
	
}

