package mx.com.kubo.model.catalogos;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Embeddable;

@Embeddable
public class IdentificationTypePK 
implements Serializable
{
	private static final long serialVersionUID = 501720355396844830L;
	
	@Column private Integer identification_type_id;
	@Column private int company_id;
	
	public Integer getIdentification_type_id() 
	{
		return identification_type_id;
	}
	
	public void setIdentification_type_id(Integer identification_type_id) 
	{
		this.identification_type_id = identification_type_id;
	}
	
	public int getCompany_id() 
	{
		return company_id;
	}
	
	public void setCompany_id(int company_id) 
	{
		this.company_id = company_id;
	}
}
