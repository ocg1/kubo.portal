package mx.com.kubo.bean;

import java.util.List;

import mx.com.kubo.model.NaturalPerson;
import mx.com.kubo.model.Phone;
import mx.com.kubo.model.ProyectLoan;

public class PersonAndPhones{
	NaturalPerson person;
	ProyectLoan proyectloan;
	List<Phone> phoneRepeat;
	List<PersonAndPhones> otherPersonPhone;
	
	public NaturalPerson getPerson() {
		return person;
	}
	public void setPerson(NaturalPerson person) {
		this.person = person;
	}
	public List<Phone> getPhoneRepeat() {
		return phoneRepeat;
	}
	public void setPhoneRepeat(List<Phone> phoneRepeat) {
		this.phoneRepeat = phoneRepeat;
	}
	public ProyectLoan getProyectloan() {
		return proyectloan;
	}
	public void setProyectloan(ProyectLoan proyectloan) {
		this.proyectloan = proyectloan;
	}
	public List<PersonAndPhones> getOtherPersonPhone() {
		return otherPersonPhone;
	}
	public void setOtherPersonPhone(List<PersonAndPhones> otherPersonPhone) {
		this.otherPersonPhone = otherPersonPhone;
	}
	
	
}
