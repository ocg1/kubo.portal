	public String porcTotalFondeo(){
		
		if( proyectFunding == null || proyectFunding.size() == 0 ){
			return dec.format(0D);
		}else{
			
			String s = montoTotalFondeo();
			
			s = s.replace("$", "");
			
			s = s.replaceAll(",", "");
			
			Double d = Double.parseDouble(s);
			
			d = (d*100)/getAmmount();
			
			s = dec.format(d);
			
			s = s.replace("$", "");
			
			return s+"%" ;
			
		}
		
	}
	
