package mx.com.kubo.bean;

import java.io.Serializable;
import java.util.List;

import javax.faces.bean.ManagedBean;
import javax.faces.bean.SessionScoped;

import mx.com.kubo.model.Change_control;

@ManagedBean
@SessionScoped
public class ShowChangeSession 
implements Serializable
{	
	private static final long serialVersionUID = 1L;
	
	private int prospectus_id;
	private int company_id;
	private int nameTable;
	private int nameField;
	
	private List<Change_control> lstChanges;
	
	public int getProspectus_id() {
		return prospectus_id;
	}
	
	public void setProspectus_id(int prospectus_id) {
		this.prospectus_id = prospectus_id;
	}
	
	public int getCompany_id() {
		return company_id;
	}
	
	public void setCompany_id(int company_id) {
		this.company_id = company_id;
	}
	
	public int getNameTable() {
		return nameTable;
	}
	
	public void setNameTable(int nameTable) {
		this.nameTable = nameTable;
	}
	
	public int getNameField() {
		return nameField;
	}
	
	public void setNameField(int nameField) {
		this.nameField = nameField;
	}
	
	public List<Change_control> getLstChanges() {
		return lstChanges;
	}
	
	public void setLstChanges(List<Change_control> lstChanges) {
		this.lstChanges = lstChanges;
	}
}
