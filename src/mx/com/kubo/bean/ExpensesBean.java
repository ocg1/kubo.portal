package mx.com.kubo.bean;

import mx.com.kubo.model.Expenses;

public class ExpensesBean 
{
	private Expenses expense;
	
	private Integer expense_type_id;
	
	private Double ammount;
	private Double excedentConsolidate;
	
	private String whyChangeData;
	private String ammount_modified;
	private String name;		
	
	protected final int EQUALS = 0;
	protected final int BEFORE = -1;
	protected final int AFTER  = 1;
	
	public ExpensesBean()
	{
		super();
	}
	
	public ExpensesBean(Integer expense_type_id,Double ammount,String name)
	{
		this.expense_type_id=expense_type_id;
		this.ammount=ammount;
		this.name=name;		
	}
	
	public Integer getExpense_type_id() 
	{
		return expense_type_id;
	}
	
	public Double getAmmount() 
	{
		return ammount;
	}
		
	public String getName() 
	{
		return name;
	}
	
	public void setName(String name) 
	{
		this.name = name;
	}
	
	public void setExpense_type_id(Integer expense_type_id) 
	{
		this.expense_type_id = expense_type_id;
	}
	
	public void setAmmount(Double ammount) 
	{
		this.ammount = ammount;
	}
	
	public Expenses getExpense() {
		return expense;
	}
	public void setExpense(Expenses expense) {
		this.expense = expense;
	}
	public String getAmmount_modified() {
		return ammount_modified;
	}
	public void setAmmount_modified(String ammount_modified) {
		this.ammount_modified = ammount_modified;
	}
	public Double getExcedentConsolidate() {
		return excedentConsolidate;
	}
	public void setExcedentConsolidate(Double excedentConsolidate) {
		this.excedentConsolidate = excedentConsolidate;
	}
	public String getWhyChangeData() {
		return whyChangeData;
	}
	public void setWhyChangeData(String whyChangeData) {
		this.whyChangeData = whyChangeData;
	}
	
	
	
}
