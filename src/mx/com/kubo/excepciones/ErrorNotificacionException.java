package mx.com.kubo.excepciones;

import mx.com.kubo.notificaciones.notificacion.NotificacionEstatusEnvio;

@SuppressWarnings("serial")
public class ErrorNotificacionException extends Exception 
{	
	public ErrorNotificacionException()
	{
		super();
	}
	
	public ErrorNotificacionException(NotificacionEstatusEnvio estatus)
	{
		super(estatus.toString());
	}	
	
	public ErrorNotificacionException(String error)
	{
		super(error);
	}
}
